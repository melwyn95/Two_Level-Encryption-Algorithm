def diffie_hellman(p, q, a, b):
    A = pow(q, a, p)
    B = pow(q, b, p)

    ## Key echange takes place A with Bob and B with Alice
    Ak = pow(B, a, p)
    Bk = pow(A, b, p)

    if Ak == Bk:
        return Ak
    else:
        return -1

